SELECT COUNTRY,
    CAMPAIGN_ID,
    CHANNEL,
    'Redemption' AS CONVERSION_TYPE,
    COUNT(DISTINCT POC_ID) AS POCS_CONVERTED,
    COUNT(DISTINCT USER_ID) AS USERS_CONVERTED,
    COUNT(DISTINCT SEND_ID) AS CONVERSIONS
FROM {{ ref('background_rewards_redemption_conversions' )}}
GROUP BY COUNTRY,
    CAMPAIGN_ID,
    CHANNEL
