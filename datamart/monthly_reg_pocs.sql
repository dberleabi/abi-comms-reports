SELECT * FROM WH.DATAMARTS_DIGITAL_COMMS.DIM_SUMM_REG_ACTIVE_USERS_POCS
WHERE MONTH >= '2021-02-01'
ORDER BY COUNTRY, MONTH

SELECT COUNTRY,
    DATE_TRUNC('MONTH', DAY) AS MONTH,
    COUNT(DISTINCT POC_ID) AS POCS
FROM WH.ANALYTICS.T_GLOBAL_ACTIVE_POCS
WHERE DAY >= '2021-02-01'
GROUP BY COUNTRY, MONTH
ORDER BY COUNTRY, MONTH
